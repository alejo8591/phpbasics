<strong>Que es Siif2Dbf?</strong>
<div class="clear"></div><br />
<p>
    <strong>Siif2Dbf</strong> es un <strong>Utilitario</strong> para generar nuevos reportes a partir de los infomes
    generados por el <strong>SIIF II NACION</strong> a tablas tipo Dbf o a otros
    archivos tipo Excel y exportar a cualquier Base de Datos,
    tambien puede exportar información a nuestra aplicación de presupuesto y de esta
    manera generar mas de 100 reportesdiferentes desde reportes básicos como listados
    por Terceros hasta reportes complejos como los reportes de reservas y cuentas por pagar o
    pueden ser tambien reportes globales de ejecución presupuestal.
</p>
<p>
Con este <strong>Utilitario</strong>  puede generar nuevos reportes en cualquier momento sin estar conectado a Internet.
</p>
